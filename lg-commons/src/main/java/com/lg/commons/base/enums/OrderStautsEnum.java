public enum OrderStautsEnum {
    //    	 消息投递状态  0 代付款 1 待发货   2 待收货  3 待评价
    PRE_PAYMENT("0", "待付款"),

    TO_BE_DELIVERED("1", "待发货"),
    PENDING_RECEIPT("2", "待收货"),
    COMMENT("3", "待评价"),
    PAY_FAIL("4", "支付失败");


    private String code;
    private String value;

    OrderStautsEnum(String code, String value) {
        this.code = code;
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public String getCode() {
        return code;
    }
}
