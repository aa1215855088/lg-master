public enum ItemStautsEnum {
    /**
     * 商品状态0 未审核 1 审核中 2 审核通过 3已驳回 4已屏蔽
     */
    UNREVIEWED("0", "未审核"),
    UNDER_REVIEW("1", "审核中"),
    EXAMINATION_PASSE("2", "审核通过"),
    DISMISSED("3", "已驳回"),
    BLOCKED("4", "已屏蔽");


    private String code;
    private String value;

    ItemStautsEnum(String code, String value) {
        this.code = code;
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public String getCode() {
        return code;
    }
}
