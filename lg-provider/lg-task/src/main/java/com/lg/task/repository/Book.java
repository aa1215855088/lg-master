@Data
@Document(indexName = "book", type = "book")
public class Book implements Serializable {
    @Id
    private Integer id;

    @Field(type = FieldType.Text, searchAnalyzer = "ik_max_word", analyzer = "ik_smart")
    private String name;

    @Field(type = FieldType.Text)
    private String color;
}
